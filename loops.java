public class loops 
{
    public static void main(String args[])
    {
        //while loop
      int i = 1;
      while (i <= 10)
      {
        System.out.println("hello");
        i++;
      }
      int x = 0;
      //do while loop
      do{
        System.out.println("hello00");
        x++;
      }while (x < 10);
    }
    
}
